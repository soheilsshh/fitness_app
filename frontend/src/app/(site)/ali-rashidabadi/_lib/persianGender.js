// Ambiguous / unisex → leave unset so default male (product rule)
["آرام", "نیکان", "نیکا", "هستی"].forEach((x) => {
  FEMALE.delete(x);
  MALE.delete(x);
});

function firstToken(fullName) {
  return String(fullName || "")
    .replace(/\u200c/g, " ")
    .replace(/[ي]/g, "ی")
    .replace(/[ك]/g, "ک")
    .replace(/[ۀة]/g, "ه")
    .replace(/[ًٌٍَُِّْ]/g, "")
    .trim()
    .split(/\s+/)
    .filter(Boolean)[0]
    ?.replace(/[-ـ]/g, "") || "";
}

/** @returns {"female"|"male"} */
export function detectGenderFromName(fullName) {
  const first = firstToken(fullName);
  if (!first) return "male";
  if (FEMALE.has(first)) return "female";
  if (MALE.has(first)) return "male";

  const FEMALE_END = ["انه", "ینه", "ازه", "وش", "ناز", "دخت", "بانو", "خاتون", "گل"];
  for (const end of FEMALE_END) {
    if (first.length > end.length + 1 && first.endsWith(end)) return "female";
  }

  const maleEndsH = new Set([
    "مصطفی", "موسی", "عیسی", "یحیی", "مرتضی", "مجتبی", "مهدی", "رضا", "علی",
    "کسری", "آریا", "سینا", "ایلیا", "طاها", "یحیى",
  ]);
  if (first.length >= 3 && (first.endsWith("ه") || first.endsWith("ا")) && !maleEndsH.has(first)) {
    return "female";
  }
  return "male";
}

export function isFemaleName(fullName) {
  return detectGenderFromName(fullName) === "female";
}
