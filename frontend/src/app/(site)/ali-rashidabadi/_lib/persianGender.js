/**
 * Rule-based Persian first-name gender (no AI). Unknown → male.
 * Large common-name coverage for Iran (~70%+ first-name hits).
 */

function n(list) {
  return new Set(
    list
      .flatMap((s) => String(s).split(/[،,|/]/))
      .map((x) =>
        x
          .replace(/\u200c/g, "")
          .replace(/[ي]/g, "ی")
          .replace(/[ك]/g, "ک")
          .replace(/[ۀة]/g, "ه")
          .trim()
      )
      .filter((x) => x.length > 1)
  );
}

